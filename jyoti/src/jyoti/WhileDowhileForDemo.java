package jyoti;

public class WhileDowhileForDemo {
	public static void main(String[] args) {
		//System.out.println("enter a no");
		
		int i=1;
		while(i>=10);{
			System.out.println(i);
			i++;
		}
		
		do{
			System.out.println(i);
			i++;
		}while(i>=10);
		
		int j=0;
		for(j=0; j<10; j++){
            System.out.println(j);			
		}
		System.out.println("end of program");
	}

}
