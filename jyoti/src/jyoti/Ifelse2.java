package jyoti;

public class Ifelse2 {
	
	public static void main(String[] args){
		
		int x=1;
		   // x=2;
		   // x=3;
		
		if(x==1){
			System.out.println("x is 1");
		}else if(x==2){
			System.out.println("x is 2");
		}else if(x==3){
			System.out.println("x is 3");
		}
		else{
			System.out.println("x is not equal to 1,2,3");
		}
		
	}

}
